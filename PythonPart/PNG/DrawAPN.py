def parse_table(text: str) -> pd.DataFrame:
    """解析带有 ± 格式的特定终端输出表格"""
    rows = []
    # 正则表达式匹配：提取 N, tau, maneuver, g, hit%, miss_mean, miss_std, time, energy
    pattern = re.compile(
        r"([\d\.]+)\s+([\d\.]+)\s+(\w+)\s+([\d\.]+)\s+([\d\.]+)\s+([\d\.]+)\s*±\s*([\d\.]+)\s+([\d\.]+)\s+([\d\.]+)"
    )
    
    for line in text.strip().splitlines():
        m = pattern.search(line)
        if m:
            rows.append({
                "N": float(m.group(1)),
                "tau": float(m.group(2)),
                "maneuver": m.group(3),
                "max_g": float(m.group(4)),
                "hit_rate": float(m.group(5)) / 100.0,  # 百分比转为 0-1 的比例
                "miss_mean": float(m.group(6)),
                "miss_std": float(m.group(7)),
                "time_mean": float(m.group(8)),
                "energy_mean": float(m.group(9))
            })
    return pd.DataFrame(rows)

