def fig_hitrate_heatmap(df):
    fig, axes = plt.subplots(2, 4, figsize=(16, 7),
                             sharex=True, sharey=True, constrained_layout=True)
    Ns   = sorted(df["N"].unique())
    taus = sorted(df["tau"].unique())
    cmap = LinearSegmentedColormap.from_list(
        "rg", ["#d73027", "#fee08b", "#1a9850"])

    for row_i, maneuver in enumerate(["step", "sine"]):
        sub = df[df["maneuver"] == maneuver]
        g_vals = sorted(sub["max_g"].unique())
        # 选 4 个代表性 g
        g_show = [g for g in [5, 10, 15, 20] if g in g_vals]
        for col_i, g in enumerate(g_show):
            ax = axes[row_i, col_i]
            subset = sub[sub["max_g"] == g]
            mat = subset.pivot_table(
                index="tau", columns="N", values="hit_rate")
            mat = mat.reindex(index=taus, columns=Ns)
            im = ax.imshow(mat.values, vmin=0, vmax=1, cmap=cmap,
                           aspect="auto", origin="lower")
            ax.set_xticks(range(len(Ns)))
            ax.set_xticklabels([f"{n}" for n in Ns], fontsize=8)
            ax.set_yticks(range(len(taus)))
            ax.set_yticklabels([f"{t}" for t in taus], fontsize=8)
            ax.set_title(f"{maneuver}, $a_T$={g}g", fontsize=11)
            # 在每个格子中标注数值
            for i in range(mat.shape[0]):
                for j in range(mat.shape[1]):
                    v = mat.values[i, j]
                    if not np.isnan(v):
                        ax.text(j, i, f"{v:.0%}", ha="center", va="center",
                                fontsize=7,
                                color="white" if v < 0.4 else "black")
    axes[0, 0].set_ylabel(r"$\tau$ (s)")
    axes[1, 0].set_ylabel(r"$\tau$ (s)")
    for ax in axes[1]:
        ax.set_xlabel("$N$")
    fig.colorbar(im, ax=axes, shrink=0.6, label="Hit Rate")
    fig.suptitle("PNG Hit Rate vs Navigation Gain $N$ and Guidance Lag $\\tau$",
                 fontsize=14, y=1.02)
    fig.savefig("fig1_hitrate_heatmap.png")
    plt.close(fig)


