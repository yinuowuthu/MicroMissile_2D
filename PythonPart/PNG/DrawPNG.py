# 6. Fig 5: 临界 τ — 50% 命中率等高线
# ──────────────────────────────────────────────
def fig_critical_tau_contour(df):
    fig, axes = plt.subplots(1, 2, figsize=(12, 5), sharey=True, constrained_layout=True)

    for ax, maneuver in zip(axes, ["step", "sine"]):
        sub = df[df["maneuver"] == maneuver]
        Ns   = sorted(sub["N"].unique())
        taus = sorted(sub["tau"].unique())
        g_vals = sorted(sub["max_g"].unique())

        # 对每个 (N, g)，插值找到使 hit_rate=0.5 的 τ
        crit = np.full((len(g_vals), len(Ns)), np.nan)
        for i, g in enumerate(g_vals):
            for j, N in enumerate(Ns):
                ss = sub[(sub["N"] == N) & (sub["max_g"] == g)].sort_values("tau")
                if len(ss) < 2:
                    continue
                hr = ss["hit_rate"].values
                ta = ss["tau"].values
                # 线性插值 hit_rate=0.5
                idx_cross = np.where(np.diff(np.sign(hr - 0.5)))[0]
                if len(idx_cross) > 0:
                    k = idx_cross[0]
                    # 线性插值
                    t0, t1 = ta[k], ta[k + 1]
                    h0, h1 = hr[k], hr[k + 1]
                    crit[i, j] = t0 + (0.5 - h0) / (h1 - h0) * (t1 - t0)
                elif hr[0] < 0.5:
                    crit[i, j] = 0.0  # 即使最小 τ 也 < 50%
                else:
                    crit[i, j] = ta[-1] + 0.1  # 始终 > 50%

        X, Y = np.meshgrid(Ns, g_vals)
        cf = ax.contourf(X, Y, crit, levels=15, cmap="RdYlGn")
        cs = ax.contour(X, Y, crit, levels=[0.1, 0.15, 0.2, 0.3, 0.5],
                        colors="k", linewidths=0.8)
        ax.clabel(cs, fmt="%.2f", fontsize=8)
        ax.set_xlabel("$N$")
        ax.set_title(f"{maneuver.capitalize()} maneuver")
    axes[0].set_ylabel("$a_T$ (g)")
    fig.colorbar(cf, ax=axes, shrink=0.75,
                 label=r"Critical $\tau$ for 50% hit rate (s)")
    fig.suptitle("PNG Guidance Lag Tolerance — "
                 "Critical $\\tau$ at 50% Hit Rate", y=1.02)
    fig.savefig("fig5_critical_tau.png")
    plt.close(fig)


