# 这里给出解析原始终端输出的辅助函数:

def parse_table(text: str) -> pd.DataFrame:
    """解析终端打印的表格文本"""
    rows = []
    for line in text.strip().splitlines():
        line = line.strip()
        if not line or line.startswith("N ") or line.startswith("---"):
            continue
        parts = [p.strip() for p in line.split("|")]
        if len(parts) != 10:
            continue
        N = float(parts[0])
        tau = float(parts[1])
        maneuver = parts[2]
        max_g_str = parts[3].strip()
        max_g = 0.0 if max_g_str == "-" else float(max_g_str)
        hr = parts[4].strip()          # "7/10"
        hit_n, hit_d = map(int, hr.split("/"))
        miss_mean = float(parts[5])
        miss_std  = float(parts[6])
        miss_max  = float(parts[7])
        time_mean = float(parts[8])
        energy    = float(parts[9])
        rows.append(dict(
            N=N, tau=tau, maneuver=maneuver, max_g=max_g,
            hits=hit_n, trials=hit_d,
            hit_rate=hit_n / hit_d,
            miss_mean=miss_mean, miss_std=miss_std, miss_max=miss_max,
            time_mean=time_mean, energy_mean=energy,
        ))
    return pd.DataFrame(rows)

